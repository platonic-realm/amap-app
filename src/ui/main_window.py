    # Load project configurations and enables the UI buttons
    def activate_selected_project(self, _selected_item):
        self.is_loading = True

        project_name = _selected_item.text()
        project_configs_path = f'./{PROJECT_DIR}/{project_name}/conf.json'
        project_configs = self.load_project_configuration(project_configs_path)

        # Inspect the project's images to decide which input options apply, and
        # persist the classification (older projects predate it; read_file and
        # the gating below rely on it).
        input_class, n_channels, input_description = classify_project_inputs(
            project_configs['source_dir'])
        self._current_input_class = input_class
        self._current_n_channels = n_channels
        config_dirty = False
        if project_configs.get('input_class') != input_class or \
                project_configs.get('n_channels') != n_channels:
            project_configs['input_class'] = input_class
            project_configs['n_channels'] = n_channels
            config_dirty = True
        # A 4D input is always a stack, so make that explicit in the config.
        if input_class == 'homogeneous_4d' and not project_configs.get('is_stacked'):
            project_configs['is_stacked'] = True
            config_dirty = True
        if config_dirty:
            self.save_project_configuration(project_configs_path, project_configs)
        self.label_input_info.setText(input_description)
        self.label_input_info.setEnabled(True)

        self.slider_cpu.setValue(project_configs['cpu_allocation'])
        self.slider_cpu.setEnabled(True)
        self.label_cpu_alloc.setEnabled(True)

        self.slider_mem.setValue(project_configs['mem_allocation'])
        self.slider_mem.setEnabled(True)
        self.label_mem_alloc.setEnabled(True)

        # Legacy projects predate the workers slider: derive a sensible default
        # from their CPU/Mem levels, bounded by this machine's worker cap.
        workers = project_configs.get(
            'num_workers',
            suggested_workers(project_configs['cpu_allocation'],
                              project_configs['mem_allocation'],
                              self.max_workers))
        self.slider_workers.setValue(workers)
        self.slider_workers.setEnabled(True)
        self.label_workers.setEnabled(True)

        # Reflect the freshly loaded slider values in the value labels.
        self._refresh_cpu_label()
        self._refresh_mem_label()
        self._refresh_workers_label()
        self._sync_value_label_states()

        self.spin_channel.setValue(project_configs['target_channel'])
        self.spin_channel.setEnabled(True)

        self.check_old_roi.setChecked(project_configs['is_old_roi'])
        self.check_old_roi.setEnabled(True)

        self.check_include_sd.setChecked(project_configs.get('does_include_sd', False))
        self.check_include_sd.setEnabled(True)

        self.check_use_gpu.setChecked(project_configs.get('use_gpu', True))
        self.check_use_gpu.setEnabled(True)

        checkpoint = project_configs.get('model_checkpoint', 'cp_10940.pth')
        idx = self.combo_checkpoint.findText(checkpoint)
        if idx >= 0:
            self.combo_checkpoint.setCurrentIndex(idx)
        self.combo_checkpoint.setEnabled(True)
        self.label_checkpoint.setEnabled(True)

        self.check_stacked.setChecked(project_configs['is_stacked'])
        self.check_stacked.setEnabled(True)
        self.label_channel.setEnabled(True)

        self.label_results.setEnabled(True)
        self.button_results_segmentation.setEnabled(project_configs['is_segmentation_finished'])
        self.button_results_morphometry.setEnabled(project_configs['is_morphometry_finished'])

        self.button_start.setEnabled(not project_configs['is_morphometry_finished'])
        self.check_stacked.setEnabled(not project_configs['is_morphometry_finished'])
        self.check_old_roi.setEnabled(not project_configs['is_morphometry_finished'])
